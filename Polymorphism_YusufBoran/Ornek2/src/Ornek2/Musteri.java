package Ornek2;

public class Musteri {
	
	private String ad,soyad;

	public Musteri(String ad, String soyad) {
		this.ad = ad;
		this.soyad = soyad;
	}

	public String getAd() {
		return ad;
	}

	public void setAd(String ad) {
		this.ad = ad;
	}

	public String getSoyad() {
		return soyad;
	}

	public void setSoyad(String soyad) {
		this.soyad = soyad;
	}
	
	
}
